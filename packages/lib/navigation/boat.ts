/**
 * Enum representing the edges of a boat.
 *
 * @enum {string}
 * @property {string} Bow - The front edge of the boat.
 * @property {string} Stern - The rear edge of the boat.
 * @property {string} Port - The left edge of the boat when facing forward.
 * @property {string} Starboard - The right edge of the boat when facing forward.
 */
export enum BoatEdge {
  Bow = "bow",
  Stern = "stern",
  Port = "port",
  Starboard = "starboard",
}
